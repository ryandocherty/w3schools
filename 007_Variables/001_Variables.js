//----------JavaScript Variables----------
//Variables are Containers for Storing Data


//JavaScript Variables can be declared in 4 ways:

    //Automatically
    //Using var
    //Using let
    //Using const


//In this first example, x, y, and z are undeclared variables.
//They are automatically declared when first used:
x = 5;          //x stores the value 5
y = 6;          //y stores the value 6
z = x + y;      //z stores the value 11


//Note - It is considered good programming practice to always declare variables before use.


//Example using var:
var a = 5;
var b = 6;
var c = a + b;

//Note: //The var keyword was used in all JavaScript code from 1995 to 2015.
        //The let and const keywords were added to JavaScript in 2015.
        //The var keyword should only be used in code written for older browsers.


//Example using let:
let d = 5;
let e = 6;
let f = d + e;

//Example using const:
const g = 5;
const h = 6;
const i = g + i;